package it.zucchetti.overload;

public class Numero {
	
	public int somma(int a, int b) {
		return a + b;
	}
	
	public double somma(double a, double b) {
		return a + b;
	}
	
	public int differenza(int a, int b) {
		return a - b;
	}
	
	public double differenza(double a, double b) {
		return a - b;
	}

}

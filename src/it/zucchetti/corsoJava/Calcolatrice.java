package it.zucchetti.corsoJava;

public class Calcolatrice {

	public double addizione(double numero, double numero2) {
		double risultato;
		risultato = numero + numero2;
		return risultato;
	}

	public double sottrazione(double numero, double numero2) {
		double risultato;
		risultato = numero - numero2;
		return risultato;
	}
	
	public double moltiplicazione(double numero, double numero2) {
		double ris;
		ris = numero * numero2;
		return ris;
	}
	
	public double divisione(double numero, double numero2) {
		double ris;
		ris = numero/numero2;
		return ris;
	}

}
